def aumentar(valor, aumento):
    porcentagem = valor + ((aumento * valor) / 100)
    return porcentagem


def diminuir(valor, reducao):
    porcentagem = valor - ((reducao * valor) / 100)
    return porcentagem


def dobro(valor):
    dobro = valor * 2
    return dobro


def metade(valor):
    divisao = valor / 2
    return divisao

