entrada1 = float(input('Digite a largura da parede: '))
entrada2 = float(input('Digite a altura da parede: '))
metrosquadrados = entrada1 * entrada2
quantidadedetinta = metrosquadrados / 2
print(f'A parede tem {metrosquadrados:.1f} metros² e serão necessários {quantidadedetinta:.1f} litros de tinta')